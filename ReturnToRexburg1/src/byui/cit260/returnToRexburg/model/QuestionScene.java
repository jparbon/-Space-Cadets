package byui.cit260.returnToRexburg.model;

import java.io.Serializable;
import java.util.Objects;

/**
 *
 * @author nataliadams
 */
public class QuestionScene extends RegularSceneType implements Serializable{
    
    //class instance variables
    private String name;
    private String type;
    private String question;
    private String answer;

    public QuestionScene() {
        super();  
    }
    
    

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getQuestion() {
        return question;
    }

    public void setQuestion(String question) {
        this.question = question;
    }

    public String getAnswer() {
        return answer;
    }

    public void setAnswer(String answer) {
        this.answer = answer;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 73 * hash + Objects.hashCode(this.name);
        hash = 73 * hash + Objects.hashCode(this.type);
        hash = 73 * hash + Objects.hashCode(this.question);
        hash = 73 * hash + Objects.hashCode(this.answer);
        return hash;
    }

    @Override
    public String toString() {
        return String.format("QuestionScene{%s, name=, type=, question=, answer=%s}", super.toString(), name, type, question, answer); 
    }
    
    

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (super.equals(obj) == false) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final QuestionScene other = (QuestionScene) obj;
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        if (!Objects.equals(this.type, other.type)) {
            return false;
        }
        if (!Objects.equals(this.question, other.question)) {
            return false;
        }
        if (!Objects.equals(this.answer, other.answer)) {
            return false;
        }
        return true;
    }
    
    
    
}
